// Helper function to get template by ID
export const getTemplateById = (templateId) => {
  return weddingTemplates.find((template) => template.id === templateId) || weddingTemplates[0];
};

// Get templates by category
export const getTemplatesByCategory = (category) => {
  return weddingTemplates.filter((template) => template.category === category);
};

// Get all unique categories
export const getCategories = () => {
  const categories = [...new Set(weddingTemplates.map((template) => template.category))];
  return categories.sort();
};

// Alias for backward compatibility
export const getTemplateCategories = getCategories;
